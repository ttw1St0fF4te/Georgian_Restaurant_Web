export const getCountries = (): Country[] => {
  return countries.sort((a, b) => a.name.localeCompare(b.name));
};

export const getCountriesWithOther = (): (Country | { code: 'OTHER'; name: 'Другая страна'; cities: [] })[] => {
  return [
    ...countries.sort((a, b) => a.name.localeCompare(b.name)),
    { code: 'OTHER', name: 'Другая страна', cities: [] }
  ];
};

export const getCitiesByCountry = (countryCode: string): string[] => {
  const country = countries.find(c => c.code === countryCode);
  return country ? country.cities.sort() : [];
};

export const getCountryByCode = (code: string): Country | undefined => {
  return countries.find(c => c.code === code);
};

export const getCountryByName = (name: string): Country | undefined => {
  return countries.find(c => c.name === name);
};